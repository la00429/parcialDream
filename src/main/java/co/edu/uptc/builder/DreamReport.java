public class DreamReport {
    private String summary;
    private String symbolicalSection;
    private String graficalInsights;

    public String getSummary() { return summary; }
    public void setSummary(String summary) { this.summary = summary; }

    public String getSymbolicalSection() { return symbolicalSection; }
    public void setSymbolicalSection(String symbolicalSection) { this.symbolicalSection = symbolicalSection; }

    public String getGraficalInsights() { return graficalInsights; }
    public void setGraficalInsights(String graficalInsights) { this.graficalInsights = graficalInsights; }
}
